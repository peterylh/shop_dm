    category_id       BIGINT        NOT NULL COMMENT '品类ID',
    category_name     VARCHAR(64)   NULL COMMENT '品类名称',
    parent_category_id BIGINT       NULL COMMENT '上级品类ID',
    category_level    TINYINT       NULL COMMENT '品类层级',
    brand             VARCHAR(64)   NULL COMMENT '品牌',
