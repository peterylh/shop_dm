    spec              VARCHAR(64)   NULL COMMENT '规格',
    barcode           VARCHAR(32)   NULL COMMENT '条形码',
    status            TINYINT       NOT NULL DEFAULT 1 COMMENT '状态'
) ENGINE=OLAP
UNIQUE KEY(product_id, etl_time)
PARTITION BY RANGE(etl_time) (
    PARTITION p20260518 VALUES LESS THAN ("2026-05-19"),
