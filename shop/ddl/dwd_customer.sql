    customer_id    BIGINT       NOT NULL COMMENT '客户ID',
    etl_time       DATETIME     NOT NULL COMMENT 'ETL处理时间',
    customer_name  VARCHAR(64)  NOT NULL COMMENT '客户姓名',
    gender         VARCHAR(4)   NULL COMMENT '性别',
    age            INT          NULL COMMENT '年龄',
    age_group      VARCHAR(16)  NULL COMMENT '年龄段:青年/中年/中老年/老年',
    phone          VARCHAR(20)  NULL COMMENT '手机号',
    email          VARCHAR(128) NULL COMMENT '邮箱',
    address        VARCHAR(256) NULL COMMENT '地址',
    city           VARCHAR(64)  NULL COMMENT '城市',
    province       VARCHAR(64)  NULL COMMENT '省份',
    member_level   VARCHAR(16)  NULL COMMENT '会员等级',
    register_date  DATE         NULL COMMENT '注册日期'
) ENGINE=OLAP
UNIQUE KEY(customer_id, etl_time)
PARTITION BY RANGE(etl_time) (
    PARTITION p20260518 VALUES LESS THAN ("2026-05-19"),
    PARTITION p20260519 VALUES LESS THAN ("2026-05-20"),
    PARTITION p20260520 VALUES LESS THAN ("2026-05-21"),
    PARTITION p20260521 VALUES LESS THAN ("2026-05-22"),
    PARTITION p20260522 VALUES LESS THAN ("2026-05-23"),
    PARTITION p20260523 VALUES LESS THAN ("2026-05-24"),
    PARTITION p20260524 VALUES LESS THAN ("2026-05-25"),
    PARTITION p_future VALUES LESS THAN MAXVALUE
)
DISTRIBUTED BY HASH(customer_id) BUCKETS 10
PROPERTIES (
    "replication_num" = "1"
);

